const card1 = document.getElementById('card1');
const audio1 =  document.getElementById('audio1');

card1.addEventListener('mouseenter', () => {
  audio1.currentTime = 0;
  audio1.play();
});

card1.addEventListener('mouseleave', () => {
  audio1.pause();
  audio1.currentTime = 0;
});

